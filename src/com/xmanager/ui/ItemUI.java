    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            getTableRowData(jTable1.getSelectedRow());
        }
        if(SwingUtilities.isRightMouseButton(evt)){
            jTable1.setComponentPopupMenu(jPopupMenu1);
            jPopupMenu1.setLocation(evt.getPoint());
            jPopupMenu1.setVisible(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        int row = jTable1.getSelectedRow();
        int result = new ItemBL().removeData(jTable1.getValueAt(row, 0).toString());
        if(result == 1){
            jTable1.setModel(new ItemsTableModel(jCombobox1.getSelectedItem().toString()));
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

