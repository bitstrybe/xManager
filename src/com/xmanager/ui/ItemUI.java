    private void jCombobox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jCombobox1ItemStateChanged
        itemCode = evt.getItem().toString();
        if (!itemCode.isEmpty()) {
            jFormattedTextField1.setText(itemCode.substring(0, 2).trim());
            jFormattedTextField1.setCaretPosition(3);
            jFormattedTextField1.requestFocusInWindow();
        }

    }//GEN-LAST:event_jCombobox1ItemStateChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int result = 0;
        Item item = new Item();
        item.setItemCode(jFormattedTextField1.getText());
        item.setItemName(itemNameField.getText());
        item.setCategory(new Category(jCombobox1.getSelectedItem().toString()));
        if (evt.getActionCommand().equals("save")) {
            result = new ItemBL().insertData(item);
        } else if (evt.getActionCommand().equals("update")) {
            result = new ItemBL().updateData(item);
        }
        if (result == 1) {
            xmu.showSuccessMessage("Item was successfully created", this);
            setTableModel(new ItemsTableModel(itemCode));
            jFormattedTextField1.setText("");
            itemNameField.setText("");
            jButton1.setText("save");
            //xmu.clearAllFields(this.getContentPane());            
        } else {
            xmu.showErrorMessage("Error creating Item", this);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

