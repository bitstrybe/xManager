    private void setTableModel(AbstractTableModel atm) {
        jTable1.setModel(atm);
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(20);
        jTable1.setRowHeight(25);
        jTable1.getTableHeader().setReorderingAllowed(false);
    }

    private void getTableRowData(int row) {
        try {
            jFormattedTextField1.setText(jTable1.getValueAt(row, 0).toString());
            itemNameField.setText(jTable1.getValueAt(row, 1).toString());
            jButton1.setText("update");
        } catch (Exception ex) {
            Logger.getLogger(SalesUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
