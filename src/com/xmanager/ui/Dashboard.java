    public void openReport(String title, String frequency) throws JRException {
        Map param = new HashMap();
        param.put("title", title);
        param.put("shop", "ALL");
        InputStream inputStream = Dashboard.class.getResourceAsStream("/com/xmanager/reports/SalesReport.jasper");
        JRTableModelDataSource jrtmds = new JRTableModelDataSource(null);
        if (frequency.equals("day")) {
            jrtmds = new JRTableModelDataSource(new SalesTableModel(frequency));
        } else if (frequency.equalsIgnoreCase("month")) {
            jrtmds = new JRTableModelDataSource(new SalesTableModel(frequency));
        }
        JasperPrint jasperPrint = JasperFillManager.fillReport(inputStream, param, jrtmds);
        ReportFrameUI reportFrame = new ReportFrameUI();
        JasperViewer jasperViewer = new JasperViewer(jasperPrint);
        reportFrame.getContentPane().add(jasperViewer.getContentPane());
        jDesktopPane1.add(reportFrame);
        reportFrame.setVisible(true);
        reportFrame.revalidate();
    }

    private JasperPrint openStockReport(String destination, String file) {
        try {
            Map param = new HashMap();
            param.put("title", "Stock in " + destination);
            InputStream inputStream = Dashboard.class.getResourceAsStream(file);
            JRTableModelDataSource jrtmds = new JRTableModelDataSource(null);
            jrtmds = new JRTableModelDataSource(new TotalStockOfItemsTableModel(destination));
            return JasperFillManager.fillReport(inputStream, param, jrtmds);
        } catch (JRException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
            return null;
        }
    }

