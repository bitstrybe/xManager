    private void makeFramesIcons() {
        JInternalFrame[] frames = jDesktopPane1.getAllFrames();
        for (JInternalFrame f : frames) {
            try {
                f.setClosed(true);
            } catch (PropertyVetoException ex) {
                Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }
