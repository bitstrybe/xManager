    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String filep = System.getProperty("file.separator");
        String PATH = System.getProperty("user.home")+filep+"Desktop"+filep;
        String date = new SimpleDateFormat("ddMMyy").format(System.currentTimeMillis());
        String executeCmd = "mysqldump -uroot -pxManagerP@$$w0rd xmanager -r "+PATH+"xmanager"+date+".sql ";
        Process runtimeProcess;
        try {

            runtimeProcess = Runtime.getRuntime().exec(executeCmd);
            int processComplete = runtimeProcess.waitFor();

            if (processComplete == 0) {
                System.out.println("Backup created successfully");                
                runtimeProcess.destroy();
                JOptionPane.showMessageDialog(this, "Backup created successfully\n Copy File: xmanager"+date+".sql FROM Desktop to drive");
            } else {                
                System.out.println("Could not create the backup");
                JOptionPane.showMessageDialog(this, "Could not create the backup");
            }
        } catch (Exception ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, PATH, ex);
            JOptionPane.showMessageDialog(this, "Could not create the backup: "+ex.getMessage());
            ex.printStackTrace();
        }

        //JOptionPane.showMessageDialog(this, "UNDER CONSTRUCTION");

    }//GEN-LAST:event_jButton4ActionPerformed

