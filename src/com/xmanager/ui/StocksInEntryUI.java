    private void getTableRowData(int row) {
        try {
            stockId = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
            jTextField1.setText(jTable1.getValueAt(row, 1).toString());
            jComboBox1.setSelectedItem(jTable1.getValueAt(row, 2));
            jFormattedTextField4.setText(jTable1.getValueAt(row, 3).toString());
            jTextField3.setText(jTable1.getValueAt(row, 4).toString());
            jDateChooser1.setDate(new SimpleDateFormat("dd-MM-yyyy").parse(new SimpleDateFormat("dd-MM-yyyy").format(jTable1.getValueAt(row, 5))));
            jButton1.setText("update");
        } catch (ParseException ex) {
            Logger.getLogger(StocksInEntryUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
