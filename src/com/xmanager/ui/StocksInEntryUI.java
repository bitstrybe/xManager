    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int result = 0;
            Stockin stockin = new Stockin();
            stockin.setItem(new Item(jTextField1.getText().trim()));
            stockin.setDestination(new Destination(jComboBox1.getSelectedItem().toString()));
            stockin.setDetails(jTextField3.getText());
            stockin.setQuantity(Integer.parseInt(jFormattedTextField4.getText().trim()));
            stockin.setDate(jDateChooser1.getDate());
            stockin.setUser(new Users(LoginUI.U.getUserid()));
            if (evt.getActionCommand().equals("save")) {
                result = new StocksInBL().insertData(stockin);
            } else if (evt.getActionCommand().equals("update")) {
                stockin.setStockinId(stockId);
                result = new StocksInBL().updateData(stockin);
                jButton1.setText("save");
            }

            if (result == 1) {
                xmu.showSuccessMessage("Stocks Data Updated Successfully", this);
                xmu.clearAllFields(jPanel1);
                jTable1.setModel(new NewStockEntryTableModel());
            } else {
                xmu.showErrorMessage("Error Updating Stock Record", this);
            }
            //stocksinwarehouse.setUnitPrice();
        } catch (Exception ex) {
            Logger.getLogger(StocksInEntryUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

