    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if (jComboBox1.getSelectedIndex() >= 0) {
            String qty;
            String warehouse = jComboBox1.getSelectedItem().toString();
            if (jComboBox1.getSelectedItem().toString().startsWith("S")) { //pls dont touch
                qty = xmu.processQtyofStockForShop(new StocksInBL().getItemsFromStockByDestination(warehouse), new SalesBL().getSalesByShopName(warehouse), warehouse, jTextField1.getText());
            } else {
               // qty = xmu.processQtyofStockForWareHouse(warehouse, jTextField1.getText());
                qty = xmu.processQtyofStockForWareHouse(new StocksInBL().getItemsFromStockByDestination(warehouse), new StocksOutBL().getStockoutByDestination(warehouse), warehouse, jTextField1.getText());
            }
            jFormattedTextField2.setText(qty);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            getTableRowData(jTable1.getSelectedRow());
        }
        if (SwingUtilities.isRightMouseButton(evt)) {
            jTable1.setComponentPopupMenu(jPopupMenu1);
            jPopupMenu1.setLocation(evt.getPoint());
            jPopupMenu1.setVisible(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jTable1MouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseDragged
        int totalqty = 0;
        int[] rows = jTable1.getSelectedRows();
        for (int i = 0; i < rows.length; i++) {
            totalqty += Integer.parseInt(jTable1.getValueAt(i, 3).toString());
        }
        jLabel1.setText("Total Quantity:" + totalqty);
    }//GEN-LAST:event_jTable1MouseDragged

